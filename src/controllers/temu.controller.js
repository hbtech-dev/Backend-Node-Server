exports.connectTemu = catchAsync(async (req, res, next) => {
  const { appKey, appSecret, accessToken, sellerId, shopName } = req.body;
  const cleanToken = (accessToken || '').trim();

  if (!cleanToken) {
    return next(new AppError('Access Token is required to connect to Temu', 400));
  }

  const mongoose = require('mongoose');
  const user = (mongoose.connection.readyState === 1 ? await User.findById(req.user.id) : null) || req.user;

  // --- Auto-resolve developer App Key & App Secret ---
  let cleanKey = (appKey || '').trim();
  let cleanSecret = (appSecret || '').trim();

  if (!cleanKey || !cleanSecret) {
    // 1. Look in existing user integrations
    const existingDev = (user.temuIntegrations || []).find(i => i.appKey && i.appSecret) || user.temuIntegration;
    if (existingDev && existingDev.appKey && existingDev.appSecret) {
      cleanKey = existingDev.appKey.trim();
      cleanSecret = existingDev.appSecret.trim();
    } else {
      // 2. Look in environment variables
      cleanKey = (process.env.TEMU_APP_KEY || '').trim();
      cleanSecret = (process.env.TEMU_APP_SECRET || '').trim();
    }
  }

  // Final check - if still not found, return descriptive error
  if (!cleanKey || !cleanSecret) {
    return next(new AppError('Developer App Credentials not configured on server. Please provide App Key & App Secret once or set them in server environment.', 400));
  }

  // --- Auto-extract Mall/Seller ID from access token ---
  // Temu tokens often end with or embed the 15-digit Mall ID
  let resolvedShopName = (shopName || '').trim();
  const mallIdMatch = cleanToken.match(/\d{10,18}/);
  if (!resolvedShopName) {
    if (mallIdMatch) {
      resolvedShopName = mallIdMatch[0];
    } else {
      resolvedShopName = `Shop-${cleanToken.slice(-6)}`;
    }
  }

  const resolvedSellerId = sellerId || (mallIdMatch ? mallIdMatch[0] : `seller-${cleanToken.slice(-6)}`);

  // ---- Attempt live Temu Open Platform Router verification ----
  const crypto = require('crypto');
  const timestamp = Math.floor(Date.now() / 1000).toString();
  const payload = {
    app_key: cleanKey,
    access_token: cleanToken,
    timestamp: timestamp,
    type: 'bg.order.list.v2.get'
  };

  const sortedKeys = Object.keys(payload).sort();
  const signStr = cleanSecret + sortedKeys.map(k => k + payload[k]).join('') + cleanSecret;
  const sign = crypto.createHash('md5').update(signStr).digest('hex').toUpperCase();

  const routerUrl = process.env.TEMU_ROUTER_URL || 'https://openapi-b-eu.temu.com/openapi/router';

  const httpFetch = require('../utils/httpHelper');

  try {
    console.log(`🔍 Connecting to Temu Router: ${routerUrl} (AppKey: ${cleanKey}, Mall: ${resolvedShopName})`);
    const testRes = await httpFetch(routerUrl, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ ...payload, sign }),
      timeout: 8000
    });

    if (!testRes.ok) {
      console.warn(`❌ Temu router HTTP Error ${testRes.status}`);
      return next(new AppError(`Temu verification failed (HTTP ${testRes.status}). Please check your credentials and server connection.`, 401));
    }

    const body = await testRes.json();
    console.log('📋 Temu Verification Response:', JSON.stringify(body));

    // Temu Open Platform uses errorCode: 1000000 or 0 or success: true to indicate SUCCESS
    const isSuccess = body.success === true || body.errorCode === 1000000 || body.errorCode === 0;
    if (!isSuccess) {
      const errorDetail = body.errorMsg || `Error code ${body.errorCode}`;
      console.warn(`❌ Temu credentials rejected: ${errorDetail}`);
      return next(new AppError(`Invalid Temu credentials: ${errorDetail}`, 401));
    }
    console.log('✅ Temu credentials verified successfully!');
  } catch (err) {
    console.error('💥 Temu API verification exception:', err.message);
    return next(new AppError(`Temu API verification failed: ${err.message}`, 401));
  }

  // ---- Save credentials ----
  const newIntegration = {
    isConnected: true,
    appKey: cleanKey,
    appSecret: cleanSecret,
    accessToken: cleanToken,
    sellerId: resolvedSellerId,
    shopName: resolvedShopName,
    lastSyncedAt: new Date()
  };

  // Add/Update in temuIntegrations array
  if (!user.temuIntegrations) {
    user.temuIntegrations = [];
  }
  const existingIdx = user.temuIntegrations.findIndex(i => i.shopName === newIntegration.shopName || i.appKey === cleanKey);
  if (existingIdx > -1) {
    user.temuIntegrations[existingIdx] = newIntegration;
  } else {
    user.temuIntegrations.push(newIntegration);
  }

  // Keep single temuIntegration as fallback/backwards compatibility
  user.temuIntegration = newIntegration;

  if (mongoose.connection.readyState === 1 && typeof user.save === 'function') {
    await user.save();
    // Trigger real API sync in background — won't block the response
    const temuSyncService = require('../services/temuSync.service');
    temuSyncService.syncUserTemuOrders(user).catch(err =>
      console.warn('Post-connect Temu sync warning:', err.message)
    );
  }

  res.status(200).json({
    status: 'success',
    message: 'Temu Seller account connected! Syncing orders in background...',
    data: {
      temuIntegration: user.temuIntegration,
      temuIntegrations: user.temuIntegrations
    }
  });
});

exports.disconnectTemu = catchAsync(async (req, res, next) => {
  const mongoose = require('mongoose');
  const user = (mongoose.connection.readyState === 1 ? await User.findById(req.user.id) : null) || req.user;
  const { shopName, appKey } = req.body;

  if (shopName || appKey) {
    // Disconnect only the specified store integration
    if (user.temuIntegrations) {
      user.temuIntegrations = user.temuIntegrations.filter(
        item => item.shopName !== shopName && item.appKey !== appKey
      );
    }
    // Update fallback if it matches the disconnected store
    if (user.temuIntegration && (user.temuIntegration.shopName === shopName || user.temuIntegration.appKey === appKey)) {
      user.temuIntegration = (user.temuIntegrations && user.temuIntegrations.length > 0)
        ? user.temuIntegrations[0]
        : { isConnected: false, appKey: '', appSecret: '', sellerId: '', shopName: '', lastSyncedAt: null };
    }
  } else {
    // Clear all integrations
    user.temuIntegrations = [];
    user.temuIntegration = {
      isConnected: false,
      appKey: '',
      appSecret: '',
      sellerId: '',
      shopName: '',
      lastSyncedAt: null
    };

    if (mongoose.connection.readyState === 1) {
      await TemuOrder.deleteMany({ user: req.user.id });
      const TemuTicket = require('../models/temuTicket.model');
      await TemuTicket.deleteMany({ user: req.user.id });
    }
  }

  if (mongoose.connection.readyState === 1 && typeof user.save === 'function') {
    await user.save();
  }

  res.status(200).json({
    status: 'success',
    message: 'Temu Store integration(s) updated successfully.',
    data: {
      temuIntegration: user.temuIntegration,
      temuIntegrations: user.temuIntegrations || []
    }
  });
});

exports.syncTemuOrders = catchAsync(async (req, res, next) => {
  const mongoose = require('mongoose');
  const user = (mongoose.connection.readyState === 1 ? await User.findById(req.user.id) : null) || req.user;

  const isConnected = (user.temuIntegration && user.temuIntegration.isConnected) || 
                      (user.temuIntegrations && user.temuIntegrations.some(i => i.isConnected));

  if (!isConnected) {
    return next(new AppError('Temu account is not connected. Please connect your Temu account first in Settings.', 400));
  }

  if (mongoose.connection.readyState === 1) {
    // Sync orders from Temu API — upserts new orders, does NOT delete existing ones
    const temuSyncService = require('../services/temuSync.service');
    await temuSyncService.syncUserTemuOrders(user);
    if (user.temuIntegration) {
      user.temuIntegration.lastSyncedAt = new Date();
    }
    if (user.temuIntegrations && user.temuIntegrations.length > 0) {
      user.temuIntegrations.forEach(i => {
        if (i.isConnected) i.lastSyncedAt = new Date();
      });
    }
    if (typeof user.save === 'function') await user.save();
  }

  const filter = { user: user._id };
  if (req.query.status) {
    filter.status = req.query.status;
  }

  const orders = await TemuOrder.find(filter).sort({ createdAt: -1 });

  res.status(200).json({
    status: 'success',
    message: 'Temu orders successfully synced!',
    data: {
      lastSyncedAt: user.temuIntegration.lastSyncedAt,
      ordersCount: orders.length,
      orders
    }
  });
});

